"""
このモジュールではPythonの変数、定数、およびさまざまなデータ構造について詳しく説明します。
基本的な型に加え、リスト（配列）、辞書（マップ）、タプル（レコード）、集合（セット）の使い方もカバーします。
また、代入とアンパック（複数の変数への値の分割）についても解説します。
"""

# **基本的な変数の定義**
# Pythonでは型を明示せずに変数を定義します。
name = "Alice"  # 文字列型
age = 30  # 整数型
height = 1.65  # 浮動小数点数型
is_student = True  # ブール型

# **定数の定義**
# Pythonには厳密な定数の概念はありませんが、大文字で命名することで定数として扱います。
PI = 3.14159
MAX_USERS = 100

# **リスト（配列）**
fruits = ["apple", "banana", "cherry"]
fruits.append("date")  # 要素の追加

# **辞書（マップ）**
student = {"name": "Alice", "age": 20, "grade": "A"}
student["major"] = "Physics"  # キーと値の追加

# **タプル（レコード）**
point = (10, 20)

# **集合（セット）**
unique_numbers = {1, 2, 3, 3, 2}  # 重複は自動的に削除
unique_numbers.add(4)

# **代入とアンパック**
x, y, z = 1, 2, 3  # 複数の変数に同時に代入
first, *rest = [10, 20, 30, 40]  # リストのアンパック
